// Fungsi untuk mendapatkan gambar komponen
function getComponentImage(type, value) {
    const componentType = ComponentImages[type];
    if (!componentType) {
        console.warn(`Component type "${type}" not found`);
        return null;
    }
    
    const imageUrl = componentType[value];
    if (!imageUrl) {
        console.warn(`Image for "${value}" in "${type}" not found`);
        return null;
    }
    
    return imageUrl;
}

// Fungsi untuk mendapatkan placeholder jika gambar tidak ditemukan
function getPlaceholderImage(type) {
    const placeholders = {
        cpu: '/images/cpu/placeholder.jpg',
        mb: '/images/mb/placeholder.jpg',
        ram: '/images/ram/placeholder.jpg',
        vga: '/images/gpu/placeholder.jpg',
        storage: '/images/ssd/placeholder.jpg',
        psu: '/images/psu/placeholder.jpg',
        case: '/images/case/placeholder.jpg',
        cooling: '/images/cool/placeholder.jpg',
        os: '/images/os/placeholder.jpg'
    };
    
    return placeholders[type] || '/images/placeholder.jpg';
}

